# Draws the DigitalCrafts logo

from turtle import *
import random
import shapes

def draw_logo():
    speed(7)
    penup()
    setpos(-300, 100)
    pendown()
    shapes.draw_trapezoid(100, pcolor='#7EC04B', fcolor='#7EC04B')
    shapes.draw_rhombus(50, pcolor='#5D8842', fcolor='#5D8842')
    shapes.draw_triangle_down(50, pcolor='#7EC04B', fcolor='#7EC04B')
    right(60)
    forward(50)
    right(60)
    shapes.draw_triangle_up(50, pcolor='#2A94CE', fcolor='#2A94CE')
    forward(50)
    shapes.draw_rhombus(50, pcolor='#1771B5', fcolor='#1771B5')
    right(60)
    shapes.draw_trapezoid(100, pcolor='#2A94CE', fcolor='#2A94CE')
    back(150)
    right(60)
    shapes.draw_triangle_down(50, pcolor='#1B67A1', fcolor='#1B67A1')
    forward(50)
    right(60)
    shapes.draw_triangle_down(50, pcolor='#467866', fcolor='#467866')
    forward(50)
    right(60)
    shapes.draw_triangle_down(50, pcolor='#1F84B1', fcolor='#1F84B1')
    forward(50)
    right(60)
    shapes.draw_triangle_down(40, pcolor='#278DC9', fcolor='#278DC9')
    penup()
    forward(60)
    right(120)
    pendown()
    shapes.draw_triangle_up(40, pcolor='#A6AAAB', fcolor='#A6AAAB')
    right(120)
    forward(10)
    right(120)
    shapes.draw_triangle_down(50, pcolor='#646B6F', fcolor='#646B6F')
    shapes.draw_triangle_up(50, pcolor='#C08258', fcolor='#C08258')
    right(300)
    shapes.draw_triangle_up(50, pcolor='#939597', fcolor='#939597')
    forward(50)
    right(120)
    shapes.draw_triangle_up(50, pcolor='#FBB231', fcolor='#FBB231')
    forward(50)
    right(120)
    back(50)
    shapes.draw_rhombus_mirror(50, pcolor='#F27830', fcolor='#F27830')
    right(120)
    forward(50)
    right(180)
    penup()
    back(100)
    right(60)
    forward(50)
    left(60)
    pendown()
    shapes.draw_trapezoid(100, pcolor='#F79E34', fcolor='#F79E34')
    right(60)
    forward(50)
    right(60)
    shapes.draw_triangle_up(50, pcolor='#BBBDBF', fcolor='#BBBDBF')
    forward(50)
    left(120)
    shapes.draw_rhombus_mirror(50, pcolor='#A6AAAB', fcolor='#A6AAAB')
    right(180)
    shapes.draw_trapezoid(100, pcolor='#BBBDBF', fcolor='#BBBDBF')

    mainloop()

draw_logo()
